#declaraçao de funçoes

#calcular area do quadrilatero
def calcular_quadrilatero(b, h):
    area = b*h
    return area

#calcular area do triangulo
def calcular_triangulo(b, h):
    area = (b*h)/2
    return area


#calcular area do trapezio
def calcular_trapezio(b,B, h):
    area = ((B*b)*h)/2
    return area

